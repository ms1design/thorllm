# в”Җв”Җ Footer line в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
print_footer() {
    local ver="${VERSION:-0.1.0}"
    local cols
    cols=$(tput cols 2>/dev/null || echo 80)
    local text=" thorllm v${ver} В· made by ms1design "
    local pad=$(( (cols - ${#text}) / 2 ))
    printf "\n%${pad}s%s\n\n" "" "${text}"
}
